public class conditional {
    public static void main(String[] args) {
        int x=10;
        int y=20;
        if(x==y){
            System.out.println(x+"x is equal to y");

        }
        else{
            if(x > y){
                System.out.println(x+ " is greater than "+ y);
            }
            else{
                System.out.println(x+"is smaller than"+ y);
            }
        }
    }
    
}
